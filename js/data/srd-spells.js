/**
 * Search spells by name substring (case-insensitive).
 */
export function searchSpells(query) {
    const q = query.toLowerCase().trim();
    if (!q) return SRD_SPELLS;
    return SRD_SPELLS.filter(s => s.name.toLowerCase().includes(q));
}

/**
 * Get a flat list of all unique levels present in the data.
 */
export function getSpellLevels() {
    return [...new Set(SRD_SPELLS.map(s => s.level))].sort((a, b) => a - b);
}

/**
 * Format a spell level for display.
 */
export function formatSpellLevel(level) {
    if (level === 0) return 'Cantrip';
    const suffix = level === 1 ? 'st' : level === 2 ? 'nd' : level === 3 ? 'rd' : 'th';
    return `${level}${suffix} Level`;
}
